import SwiftUI


extension Color {
    static var bordo: Color { return Color.init(red: 181/255, green: 23/255, blue: 1/255) }
    static var greenSmDark: Color { return Color.init(red: 0/255, green: 172/255, blue: 23/255) }
    static var greenMdDark: Color { return Color.init(red: 33/255, green: 152/255, blue: 64/255) }
}
